import 'dart:io';

/// MS17-010 永恒之蓝漏洞检测（复刻 fscan）
/// 简化版：检测 SMB 端口开放，发送探测包判断是否存在漏洞
class Ms17010Service {
  final Duration timeout;

  Ms17010Service({this.timeout = const Duration(seconds: 5)});

  /// 检测目标是否存在 MS17-010 漏洞
  /// 通过 SMB 协商请求判断 Windows 版本
  Future<bool> check(String host, {int port = 445}) async {
    try {
      final socket = await Socket.connect(host, port, timeout: timeout);
      // SMB2 negotiate request (简化)
      final negotiate = _buildSmbNegotiate();
      socket.add(negotiate);
      await socket.flush();
      final response = await socket.first;
      await socket.close();
      if (response.length < 4) return false;
      // SMB2 响应头: 0xFE 0x53 0x4D 0x42
      if (response[0] == 0xFE && response[1] == 0x53) {
        // 检查 Windows 版本（简化：有响应即可能受影响）
        return response.length >= 70;
      }
      return false;
    } catch (_) {
      return false;
    }
  }

  List<int> _buildSmbNegotiate() {
    return [
      0x00, 0x00, 0x00, 0x00, // NetBIOS 头
      0xFE, 0x53, 0x4D, 0x42, // SMB2 魔数
      0x40, 0x00, 0x00, 0x00, // 命令
      0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00,
      0x00, 0x00, 0x00, 0x00,
    ];
  }
}
